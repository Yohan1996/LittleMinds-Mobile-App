package com.example.littleminds;

public class Students {
    String TeacherName, TeacherTP, name,num, parent_name, parentTP;

    public Students()
    {

    }

    public String getTeacherName() {
        return TeacherName;
    }

    public void setTeacherName(String teacherName) {
        TeacherName = teacherName;
    }

    public String getTeacherTP() {
        return TeacherTP;
    }

    public void setTeacherTP(String teacherTP) {
        TeacherTP = teacherTP;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getParentName() {
        return parent_name;
    }

    public void setParentName(String parent_name) {
        this.parent_name = parent_name;
    }

    public String getParentTP() {
        return parentTP;
    }

    public void setParentTP(String parentTP) {
        this.parentTP = parentTP;
    }


}
